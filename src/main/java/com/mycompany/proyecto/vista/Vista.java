    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        if(txtNametitle.getText().isEmpty() || txtIntroduction.getText().isEmpty() || txtExplain.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Tiene que llenar todos los campos");
        }else{
        if(chkTerms.isSelected()){
        Cursos a = new Cursos();
        a.setVisible(true);
        a.setLocationRelativeTo(null);
        String info=txtNametitle.getText();
        Cursos.txtTitle2.setText(info);
        String info2=txtIntroduction.getText();
        Cursos.txtInt2.setText(info2);
        String info3=txtExplain.getText();
        Cursos.txtExp2.setText(info3);
        Cursos.txtDiff2.setText(cbxDiffuculty.getSelectedItem().toString());
        Controlador.eventobtnCreate();
        Cursos.txtTitle2.setEditable(false);
        Cursos.txtInt2.setEditable(false);
        Cursos.txtExp2.setEditable(false);
        Cursos.txtDiff2.setEditable(false);
        }else{
            JOptionPane.showMessageDialog(rootPane, "Tiene que aceptar los Terminos y Condiciones");
        }
        }
        
    }//GEN-LAST:event_btnCreateActionPerformed

